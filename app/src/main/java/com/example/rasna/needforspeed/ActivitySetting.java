package com.example.rasna.needforspeed;


class ActivitySetting {

    public Integer iId;
    public String iday;
    public Integer ihour;
    public Integer imin;
    public String icomment;
    public Integer iActivity;

    public ActivitySetting(Integer id, String day, Integer hour, Integer minute, String comment, Integer activity){
        iId = id;
        iday = day;
        ihour = hour;
        imin = minute;
        icomment = comment;
        iActivity = activity;

    }

}
